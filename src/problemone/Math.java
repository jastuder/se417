package problemone;

public class Math {

	public int addValues(int v1, int v2) {
		return v1 + v2;
	}
	
	public int subtractValues(int v1, int v2) {
		return v1 - v2;
	}
	
	public int multiplyValues(int v1, int v2) {
		return v1 * v2;
	}
	
	public int divideValues(int v1, int v2) {
		return v1 / v2;
	}
}
